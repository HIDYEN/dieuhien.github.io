<h2 class="carousel slide text-center">Vài nét về Trà Vinh</h2>
<p>Trà Vinh là tỉnh Duyên hải Đồng bằng sông Cửu Long, tiếp giáp với các tỉnh Bến Tre, Vĩnh Long, Sóc Trăng;Trung tâm tỉnh lỵ Trà Vinh cách thành phố Hồ Chí Minh 130 km và thành phố Cần Thơ 100 km. Tỉnh Trà Vinh có 01 thành phố, 01 thị xã và 07 huyện gồm: Thành phố Trà Vinh, thị xã Duyên Hải và các huyện Càng Long, Châu Thành, Tiểu Cần, Cầu Kè, Trà Cú, Cầu Ngang, Duyên Hải. Diện tích tự nhiên 2.341 km2, dân số trên 1,1 triệu người với 03 dân tộc chính là Kinh, Khmer, Hoa, trong đó dân tộc Khmer chiếm 30% dân số. Với trị trí tiếp giáp biển Đông chiều dài 65 km bờ biển đã hình thành nên vùng đất Trà Vinh gồm vùng đất châu thổ lâu đời, bên cạnh vùng đất trẻ mới bồi và mạng lưới sông ngòi chằng chịt mang nặng phù sa, bồi đắp cho những vườn cây ăn trái. nằm giữa sông Tiền và sông Hậu. Trà Vinh là tỉnh mưa thuận, gió hoà, nhiệt độ trung bình từ 26 – 27 độ C, hiếm khi có bão, vì thế bất cứ mùa nào trong năm du khách cũng có thể đến miền Duyên hải này.</p>
<p> Trà Vinh được đánh giá là một trong những tỉnh có tiềm năng phong phú về du lịch văn hóa, lịch sử, du lịch biển, sông nước miệt vườn, các cồn nổi ven biển chuyên canh vườn cây ăn trái đặc sản…đặc biệt là du lịch khám phá bản sắc văn hóa của vùng đất gắn bó lâu đời của ba dân tộc Kinh, Khmer, Hoa với 142 ngôi chùa Khmer có kiến trúc độc đáo trãi khắp các huyện, thị xã, thành phố trong tỉnh và các lễ hội mang đậm nét văn hóa dân tộc diễn ra quanh năm.</p>
<p>Người Trà Vinh hồn nhiên, thật thà và mến khách, chính sự thân thiện ấy đã tạo được những ấn tượng tốt đẹp trong lòng du khách, các nhà đầu tư, doanh nghiệp trong và ngoài nước khi đến với quê hương Trà Vinh.</p>
